 # Alias Name is nothing but a Short name give for Module when importing it inside our Current File.
 # Alias Name is given for easy access of the modules Attributes and functions





import exportFile as aliasName

# Syntax Breakdown

 # [import] - import is the keyword which has to be used while imoprting the file

 # [file name] = here we have to provide the file name which we want to import

 # [ as ] = "as" is the keyword which we have to use to rename the File which we have imported.

 # [ aliasName ] - aliasName is the Short name which we can use for the files which has longer name, Alias name is used for Easy access of Files.


print( aliasName )

print( aliasName.attribute_1 )



